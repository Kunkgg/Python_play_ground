def max_score(cards):
    left = 0
    right = len(cards) - 1

    def first_hand(cards, left, right):
        if left == right:
            return cards[left]
        else:
            p1 = cards[left] + second_hand(cards, left + 1, right)
            p2 = cards[right] + second_hand(cards, left, right - 1)
            return max(p1, p2)

    def second_hand(cards, left, right):
        if left == right:
            return 0
        else:
            p1 = first_hand(cards, left + 1, right)
            p2 = first_hand(cards, left, right - 1)
            return min(p1, p2)
    return max(first_hand(cards, left, right), second_hand(cards, left, right))


